    @Override
    public void actualiza(IModeloSalaDeEspera modelo, EventoSalaEspera evento, Object datos) {
        switch (evento) {
            case MOSTRAR_SALA:
                this.setVisible(true);
                break;

            case CERRAR_CU:
                this.setVisible(false);
                this.dispose();
                break;

            case ACTUALIZAR_DATOS_JUGADORES:
                if (datos instanceof String) {
                    actualizarJugadores((String) datos);
                }
                break;
            case PETICION_VOTO:
                String idCandidato = (String) datos;
                int respuesta = JOptionPane.showConfirmDialog(
                        this,
                        "El jugador '" + idCandidato + "' solicita unirse.\n¿Permitir acceso?",
                        "Votación",
                        JOptionPane.YES_NO_OPTION
                );
                boolean votoPositivo = (respuesta == JOptionPane.YES_OPTION);
                control.enviarVoto(votoPositivo);
                break;
            case PETICION_VOTO_INICIO:
                int resp = JOptionPane.showConfirmDialog(this,
                        "Un jugador quiere iniciar la partida.\n¿Estás listo?",
                        "Iniciar Partida",
                        JOptionPane.YES_NO_OPTION);

                boolean voto = (resp == JOptionPane.YES_OPTION);

                ((control.ControlSalaDeEspera) control).responderVotoInicio(voto);
                break;

            case INICIO_PARTIDA_RECHAZADA:
                JOptionPane.showMessageDialog(this, "La peticion para iniciar la partida fue denegada");
                btnIniciarPartida.setEnabled(true);
                break;

            default:
                break;
        }
    }

    private void configurarIconoVentana() {
        try {
            // 1. Intentar cargar la ruta del icono
            URL urlIcono = getClass().getResource("/imagenes/iconoJuego.png");
            if (urlIcono == null) {
                urlIcono = getClass().getResource("iImagenes/InstructivoIcon.png"); // Fallback
            }

            if (urlIcono != null) {
                // 2. Cargar la imagen original
                ImageIcon imagenOriginal = new ImageIcon(urlIcono);

                // 3. Forzar el redimensionado a un tamaño grande (ej. 256x256 píxeles)
                //    Usamos SCALE_SMOOTH para que mantenga la calidad al estirarse.
                Image imagenEscalada = imagenOriginal.getImage()
                        .getScaledInstance(600, 600, java.awt.Image.SCALE_SMOOTH);

                // 4. Asignar la imagen grande
                this.setIconImage(imagenEscalada);
            }
        } catch (Exception e) {
            System.err.println("No se pudo cargar el icono: " + e.getMessage());
        }
    }
